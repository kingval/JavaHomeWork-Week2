package HomeworkWeek2;

public class CheckPositiveOrNegativeOrZero {


    public static void main(String[] args) {

/*
Write a program to check if the number is Positive or Negative or Zero.
*/
        int num= 0 ;

        if(num>0){
            System.out.println(num + ": This is a Positive number.");
        }
        else if(num<0){
            System.out.println(num + ": This is a Negative number.");
        }
        else
            System.out.println("The number is zero.");
    }


}
